package javabasic.oop.first.constants;

public class Constants {
    // store a text with the name of each day of the week on Earth
    // final means the value cannot be changed anymore. It is final !!!
    public static final String mondayString = "Monday";
    public final static String tuesdayString = "Tuesday";
    //final can stay anywhere between the identifiers
    final public static String wednesdayString = "Wednesday";
    final public static String thursdayString = "Thursday";
    final public static String fridayString = "Friday";
    // you can change the order of the modifiers
    final static public String saturdayString = "Saturday";
    final public static String sundayString = "Sunday";
}
